        private void combobox() {
        try {
            stat = con.createStatement();
            rs = stat.executeQuery("SELECT * FROM barang");
            while (rs.next()) {
                String name = rs.getString("Nama_barang");
                cbxbarang.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "GAGAL" + e.getMessage());
        }
    }

