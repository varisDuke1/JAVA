    private static Connection buka_koneksi() {
        if (koneksi == null) {
            try {
                String url = "jdbc:mysql://localhost:3306/bioskoppd";
                String user = "root";
                String password = "";

                DriverManager.registerDriver(new com.mysql.jdbc.Driver());
                koneksi = DriverManager.getConnection(url, user, password);
            } catch (SQLException t) {
                System.out.println("Error Membuat Koneksi");
            }
        }
        return koneksi;
    }

    private void ambil_data_tabel() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = buka_koneksi();
            java.sql.Statement s = c.createStatement();
            String sql = "Select * from t_harga";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[2];
                o[0] = r.getString("kode_hari");
                o[1] = r.getString("harga");

                model.addRow(o);

            }
            r.close();
            s.close();
        } catch (SQLException q) {
            System.out.println("Masih Ada Kesalahan " + q.getMessage());
        }
    }

    private void ambil_tabel_klik() {
        int i = this.jTable1.getSelectedRow();
        String hr = (String) model.getValueAt(i, 0);
        this.hari.setText(hr);
        String hrg = (String) model.getValueAt(i, 1);
        this.harga.setText(hrg);

    }

