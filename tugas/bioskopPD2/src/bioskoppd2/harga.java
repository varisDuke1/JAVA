    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String a = hari.getText();
        String b = harga.getText();
        if (a.length() == 0 && b.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Masih Ada Yang Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else if (a.length() != 0 && b.length() == 0 && b.length() != 0) {
            JOptionPane.showMessageDialog(rootPane, "Harga Masih Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else if (a.length() == 0 && b.length() != 0) {
            JOptionPane.showMessageDialog(rootPane, "Hari Masih Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conn = null;
            String driver = "com.mysql.jdbc.Driver";
            String url = "jdbc:mysql://localhost:3306/bioskoppd";
            String user = "root";
            String password = "";
            try {
                conn = DriverManager.getConnection(url, user, password);
                java.sql.Statement stt = conn.createStatement();
                String SQL = "insert into t_harga values('" + hari.getText() + "', '" + harga.getText() + "')";
                stt.executeUpdate(SQL);
                stt.close();
                conn.close();
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                ambil_data_tabel();
                hari.setText(null);
                harga.setText(null);
            } catch (SQLException r) {
                JOptionPane.showMessageDialog(rootPane, "Masih Ada Kesalahan\n" + r.getMessage() + "", "Alert!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

