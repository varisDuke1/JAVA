    private static Connection buka_koneksi() {
        if (koneksi == null) {
            try {
                String url = "jdbc:mysql://localhost:3306/bioskoppd";
                String user = "root";
                String password = "";

                DriverManager.registerDriver(new com.mysql.jdbc.Driver());
                koneksi = DriverManager.getConnection(url, user, password);
            } catch (SQLException t) {
                System.out.println("Error Membuat Koneksi");
            }
        }
        return koneksi;
    }

    private void ambil_data_tabel() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = buka_koneksi();
            java.sql.Statement s = c.createStatement();
            String sql = "Select * from t_bangku";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[3];
                o[0] = r.getString("no_bangku");
                o[1] = r.getString("status");
                o[2] = r.getString("id_sttudio");

                model.addRow(o);

            }
            r.close();
            s.close();
        } catch (SQLException q) {
            System.out.println("Masih Ada Kesalahan " + q.getMessage());
        }
    }

    private void ambil_tabel_klik() {
        int i = this.jTable1.getSelectedRow();
        String nobang = (String) model.getValueAt(i, 0);
        this.nobangku.setText(nobang);
        String idst = (String) model.getValueAt(i, 2);
        this.idstudio.setText(idst);
        String stt = (String) model.getValueAt(i, 1);
        this.status.setText(stt);

    }

