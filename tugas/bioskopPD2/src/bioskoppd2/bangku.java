    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if (cari.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Isi Kolom Pencarian!", "Alert!", JOptionPane.ERROR_MESSAGE);
        } else {
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            try {
                Connection c = buka_koneksi();
                java.sql.Statement s = c.createStatement();
                String sql = "";
                if (jComboBox1.getSelectedIndex() == 0) {
                    sql = "select * from t_bangku where id_sttudio like '%" + cari.getText() + "%'";
                } else if (jComboBox1.getSelectedIndex() == 1) {
                    sql = "select * from t_bangku where status = '" + cari.getText() + "'";
                }
                ResultSet r = s.executeQuery(sql);

                while (r.next()) {
                    Object[] o = new Object[3];
                    o[0] = r.getString("no_bangku");
                    o[1] = r.getString("status");
                    o[2] = r.getString("id_sttudio");

                    model.addRow(o);

                }
                r.close();
                s.close();
            } catch (SQLException err) {
                JOptionPane.showMessageDialog(rootPane, "Masih Ada Kesalahan\n" + err.getMessage() + "", "Alert!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

