    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String s = idstudio.getText();
        String d = status.getText();
        String b = nobangku.getText();
        if (s.length() == 0 && d.length() == 0 && b.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Masih Ada Yang Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else if (s.length() != 0 && d.length() == 0 && b.length() != 0) {
            JOptionPane.showMessageDialog(rootPane, "ID Studio Masih Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else if (s.length() == 0 && d.length() != 0 && d.length() != 0) {
            JOptionPane.showMessageDialog(rootPane, "Status Masih Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else if (s.length() != 0 && d.length() != 0 && d.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "No Bangku Masih Kosong Masih Kosong!", "Alert", JOptionPane.ERROR_MESSAGE);
        } else {
            Connection conn = null;
            String driver = "com.mysql.jdbc.Driver";
            String url = "jdbc:mysql://localhost:3306/bioskoppd";
            String user = "root";
            String password = "";
            try {
                conn = DriverManager.getConnection(url, user, password);
                java.sql.Statement stt = conn.createStatement();
                String SQL = "insert into t_bangku values('" + nobangku.getText() + "', '" + status.getText() + "','" + idstudio.getText() + "')";
                stt.executeUpdate(SQL);
                stt.close();
                conn.close();
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                ambil_data_tabel();
                idstudio.setText(null);
                status.setText(null);
                nobangku.setText(null);
            } catch (SQLException r) {
                JOptionPane.showMessageDialog(rootPane, "Masih Ada Kesalahan\n" + r.getMessage() + "", "Alert!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

