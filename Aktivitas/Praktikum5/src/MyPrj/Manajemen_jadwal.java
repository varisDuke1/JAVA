        cbdari.setSelectedItem(dari);
        
        String menuju = tabeljadwal.getValueAt(baris, 4).toString();
        cbmenuju.setSelectedItem(menuju);
        
        String brgkt = tabeljadwal.getValueAt(baris, 5).toString();
        tfberangkat.setText(brgkt);
        
        String tiba = tabeljadwal.getValueAt(baris, 6).toString();
        tftiba.setText(tiba);
        
        String tp = tabeljadwal.getValueAt(baris, 7).toString();
        if (tipe.equalsIgnoreCase("Ekonomi")){
            rbekonomi.setSelected(true);
            rbbisnis.setSelected(false);
            rbeksekutif.setSelected(false);
        }else if (tipe.equalsIgnoreCase("Bisnis")){
            rbbisnis.setSelected(true);
            rbekonomi.setSelected(false);
            rbeksekutif.setSelected(false);
        }else{
            rbeksekutif.setSelected(true);
            rbbisnis.setSelected(false);
            rbekonomi.setSelected(false);
        }
        
        String harga = tabeljadwal.getValueAt(baris, 8).toString();
        tfharga.setText(harga);
