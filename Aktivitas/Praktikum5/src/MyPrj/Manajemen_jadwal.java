    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("No. Seri");
        model.addColumn("Maskapai");
        model.addColumn("Dari");
        model.addColumn("Menuju");
        model.addColumn("Berangkat");
        model.addColumn("Tiba");
        model.addColumn("Tipe");
        model.addColumn("harga");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM jadwal";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),
                    res.getString(3),res.getString(4),res.getString(5),res.getString(6),
                    res.getString(7),res.getString(8)});
            }
            tabeljadwal.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : "+e.getMessage());
        }
    }
    
    
